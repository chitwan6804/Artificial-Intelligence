void DFS(int graph[21][21], int node, vector<bool>& visited, int depth) {
    if (depth == 0) {
        return;
    }

    visited[node] = true;
    cout << node << " ";

    for (int i = 0; i < 21; i++) {
        if (graph[node][i] == 1 && !visited[i]) {
            DFS(graph, i, visited, depth - 1);
        }
    }
}

